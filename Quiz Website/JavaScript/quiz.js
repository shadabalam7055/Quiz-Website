const selectedQuestions = quizData.sort(() => Math.random() - 0.5).slice(0, 10);

document.addEventListener("DOMContentLoaded", function () {
  const questionEl = document.getElementById("question");
  const option_1 = document.getElementById("option_1");
  const option_2 = document.getElementById("option_2");
  const option_3 = document.getElementById("option_3");
  const option_4 = document.getElementById("option_4");
  const answersEls = document.querySelectorAll(".answer");
  const nextBtn = document.querySelector(".nextbtn");
  const clearBtn = document.querySelector(".clearbtn");
  const quizContainer = document.getElementById("quiz_section");

  let currentQuiz = 0;
  let score = 0;

  function loadQuiz() {
    deselectAnswers();
    const q = selectedQuestions[currentQuiz];
    questionEl.innerText = `${currentQuiz +1}. ${q.question}`;
    option_1.innerText = q.options[0];
    option_2.innerText = q.options[1];
    option_3.innerText = q.options[2];
    option_4.innerText = q.options[3];
  }

  function deselectAnswers() {
    answersEls.forEach((el) => (el.checked = false));
  }

  function getSelected() {
    let answer = null;
    answersEls.forEach((el) => {
      if (el.checked) {
        
        answer = el.nextElementSibling.innerText.trim();
      }
    });
    return answer;
  }

  nextBtn.addEventListener("click", () => {
    const answer = getSelected();
    if (!answer) {
      alert("âš  Please select an answer before moving to next!");
      return;
    }

    if (answer === selectedQuestions[currentQuiz].correct.trim()) {
      score++;
    }

    currentQuiz++;
    if (currentQuiz < selectedQuestions.length) {
    loadQuiz();
} else {
    quizContainer.innerHTML = `
        <div style="
            width: 320px;
            height: 320px;
            background:#e3f2fd;
            border: 2px solid #0d47a1;
            padding:20px;
            border-radius:15px;
            text-align:center;
            box-shadow:0 6px 15px rgba(0,0,0,0.15);
            font-family:Arial, sans-serif;
            display:flex;
            flex-direction:column;
            justify-content:center;
            align-items:center;
            margin:auto;
        ">
            <h2 style="margin-bottom:15px; font-size:22px; color:#0d47a1;">ðŸŽ‰ Quiz Completed</h2>
            <p style="font-size:18px; margin-bottom:20px; color:#0d47a1;">
                You scored <strong>${score}</strong> out of <strong>${selectedQuestions.length}</strong>
            </p>
            <button id="restartBtn" onclick="location.reload()">Restart Quiz</button>
        </div>
    `;

    const styleSheet = document.createElement("style");
    styleSheet.innerHTML = `
        #restartBtn {
            background-color: #1976d2;
            color: white;
            border: none;
            padding: 10px 20px;
            border-radius: 6px;
            font-size: 14px;
            cursor: pointer;
            transition: 0.3s;
            position: relative;
            animation: pulse 1.5s infinite ease-in-out;
        }
        @keyframes pulse {
            0%, 100% {
                transform: scale(1);
            }
            50% {
                transform: scale(1.05);
            }
        }
        #restartBtn:hover {
            animation-play-state: paused;
        }
    `;
    document.head.appendChild(styleSheet);
}
  });

  clearBtn.addEventListener("click", deselectAnswers);

  loadQuiz();
});